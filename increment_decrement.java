public class increment_decrement {
    public static void main(String[] args) {
        int a = 45;
        System.out.println(a++);
        System.out.println(a);
        float b = 4.56f + 46543;
        System.out.println(b++);
        System.out.println(b);
        char  cr = 'e' ;
        System.out.println(cr++);
        System.out.println(cr);

    }
}
